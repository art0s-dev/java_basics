package school.unitConversion;

public class UnitConversion {
    public static double toInches(double centimeters){ return centimeters / 2.54; }
    public static double toYards(double meters){ return meters / 0.9144; }
    public static double toMiles(double kilometers){ return kilometers / 1.609344; }
    public static double toNauticalMiles(double miles){ return miles / 1.15078; }
}
